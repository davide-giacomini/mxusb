#ifndef USB_UTIL_H
#define	USB_UTIL_H

namespace mxusb {

/**
 * \internal
 * Convert an array of two char to a short
 * \param x array of two char, in little endian format
 * \return the short
 */
inline unsigned short toShort(const unsigned char x[2])
{
    //This assumes the CPU is little endian too
    union
    {
        unsigned short theShort;
        unsigned char theChar[2];
    } conv;
    conv.theChar[0]=x[0];
    conv.theChar[1]=x[1];
    return conv.theShort;
}

} //namespace mxusb

#endif //USB_UTIL_H
